public class OOHerancaFinal {
	
	/*
	 * Classe Final não pode ser estendida.
	 * Não há herança
	 * 
	 * final em atributos - o valor não pode mudar (São Constantes)
	 * ex:
	 * public static final String URL_BLOG = "http://loiane.com";
	 * public static final String URL_CURSO_COMPLETO = "http://loiane.training";
	 * 
	 * public static void main(String [] args){
	 * 	System.out.println(Constantes.URL_BLOG);
	 * 	System.out.println(Constantes.URL_CURSO_COMPLETO);
	 *
	 * }
	 * 
	 * Convênção JAVA
	 * 
	 * Constantes são declaradas todas maiusculas e caso mais de uma palavra, 
	 * separa-se por under-line.
	 * */
	

}
