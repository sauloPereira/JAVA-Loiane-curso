package com.loiane.java.aula50;

public class ExceptionStacktraceThrows {
	
	/*
	 * classe Throwable - é a super classe dos erros
	 * 
	 * seus metodos
	 * 
	 * getMessage()  -  retorna a descrição do erro
	 * 
	 * printStackTrace()  -  imprime o stack trace do erro
	 * 		informa em qual linha da classe ocorreu o erro
	 * 
	 * 
	 * usar o Exception e não o throwable
	 * 
	 * 
	 * Erro: finaliza o programa, encerra a axecução.
	 * Exception: possibilita tratar o erro e continua a execução do programa.
	 * 
	 * 
	 * 
	 * 			Throws
	 * 
	 * Utiliza se quando não se tratat a excption - Sem try e catch
	 * 
	 * Método dispara uma exeção
	 * 
	 * Passa a responsabilidade para quem for utilizar o metodo
	 * 
	 * Exception disparadas em tempo de execução(ArrayOutOfBounds,Arithmetic)
	 * não necessitam de throws explicitamente.
	 * 
	 * -Utilizado para construção de API (bibliotecas)
	 * - e em Java Web 
	 * 
	 * */

}
